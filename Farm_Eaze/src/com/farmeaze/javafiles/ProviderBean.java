package com.farmeaze.javafiles;

public class ProviderBean {
	public String provider_name;
	public String mobile_no;
	public String product;
	public String manufacturer_year;
	public String Tractor;
	public String Pincode;
	public String village;
	public String mandal;
	public String district;
	public String state;

	public String getprovider_name() {
		return provider_name;
	}

	public void setprovider_name(String provider_name) {
		this.provider_name = provider_name;
	}

	public String getmobile_no() {
		return mobile_no;
	}

	public void setmobile_no(String mobile_no) {
		this.mobile_no = mobile_no;
	}

	public String getproduct() {
		return product;
	}

	public void setproduct(String product) {
		this.product = product;
	}

	public String getmanufacturer_year() {
		return manufacturer_year;
	}

	public void setmanufacturer_year(String manufacturer_year) {
		this.manufacturer_year = manufacturer_year;
	}

	public String getTractor() {
		return Tractor;
	}

	public void setTractor(String Tractor) {
		this.Tractor = Tractor;
	}

	public String getPincode() {
		return Pincode;
	}

	public void setPincode(String Pincode) {
		this.Pincode = Pincode;
	}

	public String getvillage() {
		return village;
	}

	public void setvillage(String village) {
		this.village = village;
	}

	public String getmandal() {
		return mandal;
	}

	public void setmandal(String mandal) {
		this.mandal = mandal;
	}

	public String getdistrict() {
		return district;
	}

	public void setdistrict(String district) {
		this.district = district;
	}

	public String getstate() {
		return state;
	}

	public void setstate(String state) {
		this.state = state;
	}
}
